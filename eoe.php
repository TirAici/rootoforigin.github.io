<h2>Current Characters</h2>
<ul class="columnsgroup">
	<li><h3>Aedric Godsford</h3>
<h3>Human - Martial Mechanist</h3>
<p>Bald and bold, with a viking like beard. Often seen in combat with his trusty longsword in one hand and a vial of some sort in the other. The second son of the noble house of Godsford exiled for his part in failing to protect his elder brother. Aedric is a man favoring quick decisive action but is not so rash as to dive into anything head first.</p>
	</li>
	<p></p>
	<li><h3>Claus Daubeny</h3>
<h3>Human - Stray Zealot</h3>
<p>Cowardly yet heroic. Claus is a weedy young man with foppish blonde hair. In battle he wields a spear in one hand and a buckler in the other, as well as ill fitting chainmail over which his tabard lies. From a young age he was raised by monks to be the "chosen one". Despite his general cowardice and unreadyness in most matters both spiritual and practical, he is actually a force for good, throwing himself in harm's way to save others without hesitation.</p>
	</li>
<p></p>
<br/>
	<li><h3>Gaius Zantori</h3>
<h3>Human - Errant Knight</h3>
<p>Stoic and Resolute. Standing taller than most and built like an ox, Gaius is a man who values honor and good conduct above all else, even his own life. In combat and bedecked in his armour he wields his halberd with deadly and versatile precision. Recently released from the service of a family he had come to see as his own, he is still finding his place in an unwelcoming world.</p>
	</li>
		<p></p>
	<li><h3>Theodwyn Vor Sarrona</h3>
<h3>High Elf - Sanguine Scholar</h3>
<p>Pragmatic and resourceful. Theo is a lean high elf with long blonde hair. Almost never seen out of his dressing gown-like robes or without a piping hot teacup close at hand. Once a slave, now no longer he spends his day acquiring knowledge and scheming to unknown ends. As a mage he specializes in controlling the flow of the fight rather than outright damage. It is unclear what he values most in life, knowledge or tea.</p>
	</li>
	
	</ul>

