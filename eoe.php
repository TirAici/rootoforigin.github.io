<p> “Yes, indeed...
It is a damned place that no longer bears any name. An orphan to all known kingdoms. A dark tower shrouded in mystery. Built in the transitory land where those banished and cast aside converge. In venturing this far beyond known borders, those exiles discover the truth behind omens long forgotten, memories of those whose footprints are now worn smooth by the unerring march of time. The land itself, feral and untamed, seems to rise up, protesting at the ambitions of foreign minds. Even within the blackstone spire unseen bells toll, stirring a deep madness from its core. And so it would seem that those outcasts now realise they stand at an uncertain precipice, that they stand - upon the Edge of Exile.”</p>
